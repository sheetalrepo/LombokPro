package basics;


public class InheritanceOriginal {
    String color;
    String wheels;

    //public default constructor
    public InheritanceOriginal() {
    }

    //getter-setters
    public String getColor() {
            return this.color;
    }

    public String getWheels() {
            return this.wheels;
    }

    public void setColor(final String color) {
            this.color = color;
    }

    public void setWheels(final String wheels) {
            this.wheels = wheels;
    }
}
